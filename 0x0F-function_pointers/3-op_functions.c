#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - Function in addition
 * @a: Integer
 * @b: Integer
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Function in subtration
 * @a: Integer
 * @b: Integer
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Function in multiplication
 * @a: Integer
 * @b: Integer
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Function in division
 * @a: Integer
 * @b: Integer
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Function in modulo
 * @a: Integer
 * @b: Integer
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

